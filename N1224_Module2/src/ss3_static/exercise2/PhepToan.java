package ss3_static.exercise2;

public class PhepToan {
    public static int tong(int a, int b) {
        return a + b;
    }

    public static int tru(int a, int b) {
        return a - b;
    }

    public static int nhan(int a, int b) {
        return a * b;
    }

    public static double chia(int a, int b) {
        return (double) a / b;
    }
}
