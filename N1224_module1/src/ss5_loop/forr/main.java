package ss5_loop.forr;

public class main {
    public static void main(String[] args) {
        int a = 5;

        for (int i = 0; i < a; i++) {
            System.out.println(i);
        }

        int i = 0;
        while (i < a) {
            System.out.println(i);
            i++;
        }

    }
}
